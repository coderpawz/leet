/**
 * @param {character[][]} grid
 * @return {number}
 */
var maxKilledEnemies = function(grid) {
  if (!grid || !grid.length || !grid[0].length) {
    return 0;
  }

  let maxRow;
  let maxCol = [];
  let max = 0;
  for (let rr = 0; rr < grid.length; rr++) {
    for (let cc = 0; cc < grid[0].length; cc++) {
      if (!cc || grid[rr][cc - 1] === 'W') {
        maxRow = 0;
        for (let ii = cc; ii < grid[0].length && grid[rr][ii] !== 'W'; ii++) {
          if (grid[rr][ii] === 'E') {
            maxRow++;
          }
        }
      }
      if (!rr || grid[rr - 1][cc] === 'W') {
        maxCol[cc] = 0;
        for (let ii = rr; ii < grid.length && grid[ii][cc] !== 'W'; ii++) {
          if (grid[ii][cc] === 'E') {
            maxCol[cc]++;
          }
        }
      }
      if (grid[rr][cc] === '0') {
        max = Math.max(max, maxRow + maxCol[cc]);
      }
    }
  }

  return max;
};

